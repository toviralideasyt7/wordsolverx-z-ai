import { existsSync, mkdirSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { PRERENDER_ENTRIES } from '../src/lib/route-registry.js';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, '..');
const outputPath = path.join(projectRoot, 'src', 'lib', 'generated', 'sitemap-lastmod.ts');

const WORDLEBOT_VARIANT_SOLVER_ROUTES = new Set([
	'/canuckle-solver',
	'/quordle-solver',
	'/dordle-solver',
	'/octordle-solver',
	'/thirdle-solver',
	'/hardle-solver',
	'/warmle-solver',
	'/woodle-solver',
	'/w-peaks-solver',
	'/xordle-solver',
	'/fibble-solver',
	'/spotle-wordle-solver'
]);

function normalizeDate(date) {
	return date.toISOString().slice(0, 10);
}

function buildRouteCandidates(route) {
	const routePart = route === '/' ? '' : route.replace(/^\/+/, '');
	const candidates = [];
	const baseRouteDirs = ['', '(content)', '(interactive)'];
	const sourceFiles = ['+page.svelte', '+page.server.ts', '+page.ts'];

	for (const baseDir of baseRouteDirs) {
		for (const sourceFile of sourceFiles) {
			const target = route === '/'
				? path.join(projectRoot, 'src', 'routes', sourceFile)
				: path.join(projectRoot, 'src', 'routes', baseDir, routePart, sourceFile);
			candidates.push(target);
		}
	}

	if (/^\/\d+-letter-wordle-solver$/.test(route)) {
		for (const sourceFile of sourceFiles) {
			candidates.push(
				path.join(
					projectRoot,
					'src',
					'routes',
					'(interactive)',
					'[wordLength=wordlebotLength]-letter-wordle-solver',
					sourceFile
				)
			);
		}
	}

	if (WORDLEBOT_VARIANT_SOLVER_ROUTES.has(route)) {
		for (const sourceFile of sourceFiles) {
			candidates.push(
				path.join(
					projectRoot,
					'src',
					'routes',
					'(interactive)',
					'[variant=wordlebotVariant]-solver',
					sourceFile
				)
			);
		}
	}

	return [...new Set(candidates)];
}

function getRouteLastModified(route) {
	const mtimes = buildRouteCandidates(route)
		.filter((candidate) => existsSync(candidate))
		.map((candidate) => statSync(candidate).mtime);

	if (mtimes.length === 0) {
		return null;
	}

	const latest = mtimes.reduce((currentLatest, candidate) =>
		candidate.getTime() > currentLatest.getTime() ? candidate : currentLatest
	);

	return normalizeDate(latest);
}

function generateManifest() {
	const entries = Object.fromEntries(
		PRERENDER_ENTRIES.map((route) => [route, getRouteLastModified(route)]).filter(
			([, lastmod]) => Boolean(lastmod)
		)
	);

	const output = `// Generated by scripts/generate-sitemap-lastmod.mjs. Do not edit manually.\n` +
		`export const GENERATED_SITEMAP_LASTMOD: Record<string, string> = ${JSON.stringify(entries, null, '\t')};\n`;

	mkdirSync(path.dirname(outputPath), { recursive: true });
	writeFileSync(outputPath, output, 'utf8');
	console.log(`[sitemap-lastmod] Wrote ${path.relative(projectRoot, outputPath)}`);
}

generateManifest();
